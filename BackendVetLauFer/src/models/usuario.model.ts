import {Entity, model, property, hasMany, hasOne} from '@loopback/repository';
import {Mascota} from './mascota.model';
import {ProductoServicio} from './producto-servicio.model';
import {Rol} from './rol.model';

@model()
export class Usuario extends Entity {
  @property({
    type: 'string',
    id: true,
    generated: true,
  })
  id?: string;

  @property({
    type: 'string',
    required: true,
  })
  cedula: string;

  @property({
    type: 'string',
    required: true,
  })
  nombre: string;

  @property({
    type: 'string',
    required: true,
  })
  apellido: string;

  @property({
    type: 'string',
    required: true,
  })
  telefono: string;

  @property({
    type: 'string',
    required: true,
  })
  correo: string;

  @property({
    type: 'string',
    required: true,
  })
  contrasena: string;

  @property({
    type: 'string',
    required: true,
  })
  direccion: string;

  @hasMany(() => Mascota)
  mascotas: Mascota[];

  @hasMany(() => ProductoServicio)
  productoServicios: ProductoServicio[];

  @hasOne(() => Rol)
  rol: Rol;

  constructor(data?: Partial<Usuario>) {
    super(data);
  }
}

export interface UsuarioRelations {
  // describe navigational properties here
}

export type UsuarioWithRelations = Usuario & UsuarioRelations;
